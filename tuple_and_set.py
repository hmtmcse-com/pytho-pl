# Tuple Example
tuple_data = (1, 2, "name")
print(tuple_data)


# Set Example
set_data = {1, 2, "name"}
print(set_data)

# Add element into Set Example
set_data.add("data")
print(set_data)

# Remove element by index into Set Example
set_data.remove(1)
print(set_data)

# Or
set_data.discard(1)
print(set_data)


